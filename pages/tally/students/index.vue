<script setup lang="ts">
import { ref, onMounted } from "vue";
import JumpButton from "../components/JumpButton.vue";
import PageHeader from "../components/PageHeader.vue";
import { Student, Session } from "../../types";
import Loader from "../components/Loader.vue";
import { setErrorToast, setToast } from "~/composables/toastManager";
import SubPanel from "../components/SubPanel.vue";
import Input from "../components/Input.vue";
import Button from "../components/Button.vue";
import moment from "moment";

const students = ref([] as (Student & Session)[]);
const tally_from = ref("");
const tally_to = ref("");

const updateDataSet = async () => {
  try{
    const data = await window.api.executeQuery(
      `SELECT
        students.id,
        students.first_name || ' ' || students.last_name as student_name,
        students.suspended,
        COUNT(*) as session_count,
        SUM(sessions.duration) as session_time,
        SUM(
          CASE WHEN duration < 1 THEN duration * sessions.price * sessions.price_factor_below_1
            ELSE duration * sessions.price
          END
        ) as session_value
      FROM students
        LEFT JOIN sessions ON student_id = students.id
      WHERE date(session_date) >= (SELECT date(value) FROM settings WHERE name = 'tally_from')
        AND date(session_date) <= (SELECT date(value) FROM settings WHERE name = 'tally_to')
      GROUP BY students.id
      HAVING COUNT(sessions.id) > 0
      ORDER BY students.suspended, students.first_name, students.last_name`
    );
    students.value = data;
  }catch(err){
    setErrorToast("Błąd wczytywania statystyk", err)
  }

  //tally from/to
  try{
    const data = await window.api.getSetting("tally_from");
    tally_from.value = data.value;
  }catch(err){
    setErrorToast("Błąd wczytywania ustawień", err)
  }
  try{
    const data = await window.api.getSetting("tally_to");
    tally_to.value = data.value;
  }catch(err){
    setErrorToast("Błąd wczytywania ustawień", err)
  }
}

onMounted(async () => {
  updateDataSet();
});

const updateFilters = (name: string, val: string) => {
  if (val.charAt(0) == "0") return;

  window.api.executeQuery(
    `UPDATE settings SET value = ? WHERE name = ?`,
    [val, `tally_${name}`]
  ).then(res => {
    setToast("Zakres filtrów zmieniony")
    updateDataSet();
  })
  .catch(err => setErrorToast("Nie udało się zmienić filtrów", err.message))
}
const setReportYear = (year: number) => {
  updateFilters("from", `${year}-01-01`)
  updateFilters("to", `${year}-12-31`)
}
</script>

<template>
  <PageHeader title="Policzenie uczniów">
    <JumpButton :to="{name: 'Tally'}" icon="angles-left"></JumpButton>
  </PageHeader>

  <p class="ghost">
    Poniżej widzisz zestawienie wpływów od uczniów na podstawie całej dostępnej historii.
    Szczegóły dla każdego ucznia z osobna możesz znaleźć pod przyciskami obok.
  </p>

  <SubPanel title="Zakres zestawienia">
    <div class="flex-right h-center inputs-in-line">
      <Input type="date" name="date_from" :value="tally_from" label="Od" @change="updateFilters('from', $event.target.value)"/>
      <Input type="date" name="date_to" :value="tally_to" label="Do" @change="updateFilters('to', $event.target.value)" />
      <Button icon="calendar" @click="(ev) => setReportYear(moment().year())">{{ moment().year() }}</Button>
      <Button icon="calendar" @click="(ev) => setReportYear(moment().year() - 1)">{{ moment().year() - 1 }}</Button>
    </div>
  </SubPanel>

  <template v-if="students">
    <table v-if="students.length" class="rounded">
      <thead>
        <tr>
          <th>Uczeń</th>
          <th>Sesje</th>
          <th>Czas</th>
          <th>Suma</th>
          <th></th>
        </tr>
      </thead>
      <tbody>
        <tr v-for="student in students" :key="student.id" :class="{ghost: student.suspended}">
          <td>{{ student.student_name }}</td>
          <td>{{ student.session_count }}</td>
          <td>{{ student.session_time }} h</td>
          <td>{{ $toPln(student.session_value) }}</td>
          <th>
            <div class="flex-right h-center action-buttons">
              <JumpButton title="Szczegóły" icon="eye" :to="{name: 'TallyStudentDetails', params: {id: student.id}}" />
            </div>
          </th>
        </tr>
      </tbody>
      <tfoot>
        <tr>
          <th colspan="4"></th>
          <th>
            <fai :icon="['fas', 'calculator']" title="Liczba wyników" />
            {{ students.length }}
          </th>
        </tr>
      </tfoot>
    </table>
    <p v-else>
      Lista jest pusta. Brakuje uczniów lub sesji dla nich.
    </p>
  </template>
  <Loader v-else />
</template>

